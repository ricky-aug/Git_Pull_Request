'''
This file describe the String function with example
'''
s="how are you"

# return the length of the string
print(len(s))  #the length is 11

#title() return the string which contains of First character of string in Capital
print(s.title())  # How are you


# upper() return the string in upper case
print(s.upper()) #HOW ARE YOU

#isupper return true or false, whether the string is in upper case or not
print(s.isupper()) #False
